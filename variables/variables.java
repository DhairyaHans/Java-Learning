class Variable{
    public static void main(String args[]){
        // Integers
        byte num_byte = 20;
        short num_short = 123;
        int num_int = 10;
        long num_long = 5370l;

        // Float
        float num_float = 5.6f;
        double num_double = 5.66;

        // Character
        char ch = 'K';

        // Boolean
        boolean is_true = true;
    }       
}